package com.liwj.szpd.vo;

public class ProcessWarnVO {
    private int no;
    private int total;
    private int outDate;
    private int month;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getOutDate() {
        return outDate;
    }

    public void setOutDate(int outDate) {
        this.outDate = outDate;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }
}
